import { calcLineItem } from "@/types/invoices";
import type { Invoice, InvoiceLineItem } from "@/types/invoices";

function li(
  id: string,
  type: InvoiceLineItem["type"],
  name: string,
  quantity: number,
  price: number,
  taxRate: number,
  note = ""
): InvoiceLineItem {
  const unit = type === "Time" ? "hrs" : type === "Mileage" ? "mi" : type === "Expense" ? "item" : "units";
  return calcLineItem({ id, type, name, note, quantity, unit, price, taxRate });
}

function histEntry(id: string, date: string, action: string) {
  return { id, date, action, by: "You" };
}

function payEntry(
  id: string,
  amount: number,
  paymentDate: string,
  method: Invoice["payments"][0]["method"],
  note = ""
) {
  return {
    id,
    amount,
    currency: "USD",
    paymentDate,
    method,
    note,
    status: "Paid" as const,
    createdAt: `${paymentDate}T10:00:00Z`,
  };
}

